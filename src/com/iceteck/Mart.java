package com.iceteck;

public class Mart {

	private Integer id;
	private String name;
	private String dateadded;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer categoryid) {
		this.id = categoryid;
	}
	public String getName() {
		return name;
	}
	public void setName(String categoryname) {
		this.name = categoryname;
	}
	public String getDateadded() {
		return dateadded;
	}
	public void setDateadded(String categorydateadded) {
		this.dateadded = categorydateadded;
	}
	
	
}
